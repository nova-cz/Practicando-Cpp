#include <iostream>
using namespace std;

int main(){

  int a = 10, b = 3;
  int suma = a + b; // 13
  int resta = a - b; // 7
  int producto = a * b; // 30
  int division = a / b; // 3
  int modulo = a % b; // 1

  cout << "Suma: " << suma << endl;
  cout << "Resta: " << resta << endl;
  cout << "Producto: " << producto << endl;
  cout << "Division: " << division << endl;
  cout << "Modulo: " << modulo << endl;


  return 0;
}



